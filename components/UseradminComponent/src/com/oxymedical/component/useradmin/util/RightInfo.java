package com.oxymedical.component.useradmin.util;

public class RightInfo 
{

	String companyId = null;
	String name = null;
	String languageProperties = null;
	String defaultURL = null;
	/**
	 * @return the defaultURL
	 */
	public String getDefaultURL() {
		return defaultURL;
	}
	/**
	 * @param defaultURL the defaultURL to set
	 */
	public void setDefaultURL(String defaultURL) {
		this.defaultURL = defaultURL;
	}
	/**
	 * @return the companyId
	 */
	public String getCompanyId() {
		return companyId;
	}
	/**
	 * @param companyId the companyId to set
	 */
	public void setCompanyId(String companyId) {
		this.companyId = companyId;
	}
	/**
	 * @return the languageProperties
	 */
	public String getLanguageProperties() {
		return languageProperties;
	}
	/**
	 * @param languageProperties the languageProperties to set
	 */
	public void setLanguageProperties(String languageProperties) {
		this.languageProperties = languageProperties;
	}
	/**
	 * @return the name
	 */
	public String getName() {
		return name;
	}
	/**
	 * @param name the name to set
	 */
	public void setName(String name) {
		this.name = name;
	}
}
