package com.oxymedical.component.useradmin.exception;

public class NoSuchRightException extends Exception
{

	public NoSuchRightException() 
	{
		super();
	}

	public NoSuchRightException(String msg) 
	{
		super(msg);
	}

	public NoSuchRightException(String msg, Throwable cause) 
	{
		super(msg, cause);
	}
	public NoSuchRightException(Throwable cause) 
	{
		super(cause);
	}

	

	
}
