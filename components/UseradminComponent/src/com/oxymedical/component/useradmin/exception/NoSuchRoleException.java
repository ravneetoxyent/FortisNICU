package com.oxymedical.component.useradmin.exception;



public class NoSuchRoleException extends UAComponentException
{
 

		public NoSuchRoleException() 
		{
			super();
		}

		public NoSuchRoleException(String msg) 
		{
			super(msg);
		}

		public NoSuchRoleException(String msg, Throwable cause) 
		{
			super(msg, cause);
		}

		public NoSuchRoleException(Throwable cause) 
		{
			super(cause);
		}

}

