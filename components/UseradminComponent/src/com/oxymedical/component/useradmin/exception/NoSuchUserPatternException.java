package com.oxymedical.component.useradmin.exception;

public class NoSuchUserPatternException extends UAComponentException
{
	public NoSuchUserPatternException() 
	{
		super();
	}

	public NoSuchUserPatternException(String msg) 
	{
		super(msg);
	}

	public NoSuchUserPatternException(String msg, Throwable cause) 
	{
		super(msg, cause);
	}

	public NoSuchUserPatternException(Throwable cause) 
	{
		super(cause);
	}

}
